public class Thenth {
public static void main(String[] args) {
	
	for(int i=1;i<=4;i++) {
		for(int j=2;j<=i;j++) {
			System.out.print(" ");
		}
		for(int c =4;c>=i;c--) {
			System.out.print("*");
			
		}
		for(int y=3;y>=i;y--) {
			System.out.print("*");
		}
		
		
		
		
		
		
		System.out.println();
	}
	
	
}
}
