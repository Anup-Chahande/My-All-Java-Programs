package super20practice;

public class Swap {

	public static void main(String[] args) {
		
		int a = 2;
		int b=3;
		System.out.println("before swapping value of a is " +a + " and b is " + b);
		int c = a;
		a=b;
		b=c;
		System.out.println("After swapping value of a is " +a + " and b is " + b);
		
	}
	
	
}
